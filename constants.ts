// Helper to construct events efficiently
const createEvent = (
  title: string, narrative: string, 
  optA_Label: string, optA_Stat: any, optA_Val: number, optA_Succ: any, optA_Fail: any,
  optB_Label: string, optB_Stat: any, optB_Val: number, optB_Succ: any, optB_Fail: any
): GameEvent => {
  return {
    title, narrative,
    options: [
      { 
        label: optA_Label, reqText: `${optA_Stat === 'none' ? '无' : (optA_Stat === 'martial' ? '武力' : optA_Stat === 'strategy' ? '智谋' : optA_Stat === 'wealth' ? '财富' : '威望')} ${optA_Val > 0 ? '≥ '+optA_Val : ''}`, 
        checkStat: optA_Stat, checkVal: optA_Val, success: optA_Succ, fail: optA_Fail 
      },
      { 
        label: optB_Label, reqText: `${optB_Stat === 'none' ? '无' : (optB_Stat === 'martial' ? '武力' : optB_Stat === 'strategy' ? '智谋' : optB_Stat === 'wealth' ? '财富' : '威望')} ${optB_Val > 0 ? '≥ '+optB_Val : ''}`, 
        checkStat: optB_Stat, checkVal: optB_Val, success: optB_Succ, fail: optB_Fail 
      }
    ]
  };
};

